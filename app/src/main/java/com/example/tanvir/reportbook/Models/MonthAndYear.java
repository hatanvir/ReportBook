package com.example.tanvir.reportbook.Models;

public class MonthAndYear {
    private String month,year;

    public MonthAndYear(String month, String year) {
        this.month = month;
        this.year = year;
    }

    public String getMonth() {
        return month;
    }

    public String getYear() {
        return year;
    }
}
